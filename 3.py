# Чтение символов из файла
with open('3.txt', 'r') as s:
    x = s.read()

# Разделение объектов по пробелам
objects = x.split()

# Создание словаря для преобразования цифр в слова
num_to_word = {
    '0': 'ноль', '1': 'один', '2': 'два', '3': 'три', '4': 'четыре',
    '5': 'пять', '6': 'шесть', '7': 'семь'
}

# Обработка каждого объекта
for a in objects:
    try:
        # Проверка, является ли объект восьмеричным числом и не превышает 2048
        if int(a, 8) <= 2048:
            # Преобразование числа в слова
            words = ' '.join(num_to_word[digit] for digit in a)
            print(f"Восьмеричное число: {a}")
            print(f"В словах: {words}")
    except ValueError:
        continue
